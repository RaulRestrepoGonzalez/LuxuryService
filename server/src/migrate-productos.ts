async function main() {
  const db = await connectDb();
  const collection = db.collection('productos');

  const lines = RAW.trim().split('\n');
  const headers = lines[0].split('\t');
  const proveedorIdx = headers.indexOf('PROVEEDOR');
  const productoIdx = headers.indexOf('PRODUCTO');
  const tipoIdx = headers.indexOf('TIPO');
  const claseIdx = headers.indexOf('CLASE');
  const precioIdx = headers.indexOf('VALOR_VENTA');
  const stockIdx = headers.indexOf('EXISTENCIA');

  const rows: any[] = [];

  for (let i = 1; i < lines.length; i++) {
    const cells = lines[i].split('\t');
    const nombre = (cells[productoIdx] || '').trim();
    const precio = Number(String(cells[precioIdx] || '0').replace(/[^0-9.]/g, '')) || 0;
    const stock = Number(String(cells[stockIdx] || '0').replace(/[^0-9.]/g, '')) || 0;
    const categoria = (cells[claseIdx] || 'XTODOS').trim();
    const proveedor = (cells[proveedorIdx] || '').trim();

    if (!nombre) continue;

    rows.push({ nombre, descripcion: proveedor, precio, stock, categoria });
  }

  const seen = new Map<string, any>();
  for (const r of rows) {
    const existing = seen.get(r.nombre);
    if (!existing || r.stock > existing.stock) {
      seen.set(r.nombre, r);
    }
  }

  const docs = [...seen.values()]
    .filter(d => d.stock > 0)
    .map(d => ({
      nombre: d.nombre,
      descripcion: d.descripcion,
      precio: d.precio,
      stock: d.stock,
      categoria: d.categoria,
      icono: 'inventory_2',
      color: '#4285F4',
      activo: true,
      created_at: new Date()
    }));

  await collection.deleteMany({});
  await collection.insertMany(docs);

  const categorias = [...new Set(docs.map(d => d.categoria))];

  console.log(`\n✓ Productos importados: ${docs.length}`);
  console.log(`  Categorías: ${categorias.join(', ')}`);
  process.exit(0);
}

main().catch(err => { console.error(err); process.exit(1); });
